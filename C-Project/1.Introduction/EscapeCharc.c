#include<stdio.h>

int main(){
    printf("Hello \n World"); // '\n' inticates next line
    printf("Hello\bWorld"); // '\b' inticates backspace
    printf("Hello\fWorld");// '\f' indicates form feed
    printf("Hello World \r"); // '\r' 	Return
    printf("Hello World \r"); // '\r' 	Return
    printf("Hello \t World "); // '\t' Horizontal tab
    printf("Hello \v World"); // '\v' Vertical Tab
    printf("Hello \\ World");// '\\'	Backslash
    printf("Hello \' World");// '\''	Single quotation mark
    printf("Hello \" World");// '\"'	Double quotation mark
    printf("Hello \? World");// '\?'	Question mark
    // printf("\0"); // '\0' 	Null character
    return 0;




}